public class Sepatu {
    private String id_sepatu;
    private String nama_sepatu;
    private String jenis;

    public String getJenis() {
        return jenis;
    }

    public void setJenis(String jenis) {
        this.jenis = jenis;
    }

    public String getId_sepatu() {
        return id_sepatu;
    }

    public void setId_sepatu(String id_sepatu) {
        this.id_sepatu = id_sepatu;
    }

    public String getNama_sepatu() {
        return nama_sepatu;
    }

    public void setNama_sepatu(String nama_sepatu) {
        this.nama_sepatu = nama_sepatu;
    }

}
